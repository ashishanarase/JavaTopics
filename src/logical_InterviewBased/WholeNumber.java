package logical_InterviewBased;

public class WholeNumber {	
	//A whole number is simply any positive number that does not include a fractional or decimal part.

	public static void  main(String[] args) {

		int a = 15;

		if (a>=0) {
			System.out.println("Number is whole number");			
		}
		else {
			System.out.println("Number is not whole number");
		}

	}
}



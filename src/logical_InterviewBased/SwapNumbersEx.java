package logical_InterviewBased;

public class SwapNumbersEx {
	//swap two numbers using third variable	
	public static void main(String [] args) {
		int a = 22; 
		int	b = 77;
		int t;
		
		t=a;
		a=b;
		b=t;
		System.out.println("a = "+a);
		System.out.println("b = "+b);
				
	}

}
 
package logical_InterviewBased;

public class TableEx {
 
	public static void main(String[] args) {
		//Java Program to Generate Multiplication Table(1 to 10)

		int a;
		for (a=1;a<=10;a++)
		{
			for(int b=1;b<=10;b++)
			{
				System.out.print(a*b+ " ");
			}
			System.out.println();
		}
	}
}


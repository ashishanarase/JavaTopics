package operators;

public class UnaryOperators {
	public static void main(String [] args) {
		int a = 50;
		System.out.println(a); 			//50
		System.out.println(a++); 		//50-51
		System.out.println(a--); 		//51-50
		System.out.println(a);  		//50
		System.out.println(++a);		//51
		System.out.println(--a);		//50
		System.out.println(a);			//50
	}
	
}
